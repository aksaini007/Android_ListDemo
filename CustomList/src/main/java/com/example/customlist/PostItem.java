package com.example.customlist;

public class PostItem {

    Integer id;
    String heading;
    String description;
    Integer channelLogoResId;
    String channelName;
    String dateTime;
    Integer postResId;
    String link;
    String likes;
    String comments;
    String shares;

    public Integer getChannelLogoResId() {
        return channelLogoResId;
    }

    public void setChannelLogoResId(Integer channelLogoResId) {
        this.channelLogoResId = channelLogoResId;
    }

    public String getChannelName() {
        return channelName;
    }

    public void setChannelName(String channelName) {
        this.channelName = channelName;
    }

    public String getDateTime() {
        return dateTime;
    }

    public void setDateTime(String dateTime) {
        this.dateTime = dateTime;
    }

    public Integer getPostResId() {
        return postResId;
    }

    public void setPostResId(Integer postResId) {
        this.postResId = postResId;
    }

    public String getLink() {
        return link;
    }

    public void setLink(String link) {
        this.link = link;
    }

    public String getLikes() {
        return likes;
    }

    public void setLikes(String likes) {
        this.likes = likes;
    }

    public String getComments() {
        return comments;
    }

    public void setComments(String comments) {
        this.comments = comments;
    }

    public String getShares() {
        return shares;
    }

    public void setShares(String shares) {
        this.shares = shares;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getHeading() {
        return heading;
    }

    public void setHeading(String heading) {
        this.heading = heading;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
}
